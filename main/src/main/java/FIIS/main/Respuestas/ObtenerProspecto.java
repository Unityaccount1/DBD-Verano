package FIIS.main.Respuestas;

public class ObtenerProspecto {
    private int codigo;
    private String apelpat,apelmat,nombre,estado,fecha;

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getApelpat() {
        return apelpat;
    }

    public void setApelpat(String apelpat) {
        this.apelpat = apelpat;
    }

    public String getApelmat() {
        return apelmat;
    }

    public void setApelmat(String apelmat) {
        this.apelmat = apelmat;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }
}
